<h1>NBA: Kevin Durant confessa não se importar mais com legado</h1>

<img src="img/durant.jpeg" width="1000px"> </br>

<p>
Jogador do Phoenix Suns disse que já se importou mais com o legado que vai deixar nas quadras, mas hoje em dia só quer dar o melhor em quadra e curtir com a família.
</p>


<p>
    Os torcedores adoram debater sobre os melhores jogadores da história da NBA, quem está em uma fase boa e quem caiu de produção. No entanto, isto pode ser um grande peso para os próprios jogadores. Kevin Durant disse que, hoje em dia, não se importa mais com o legado que vai deixar. </br> 
    Em uma entrevista ao jornal “The Athletic”, o jogador do Phoenix Suns comentou também que o objetivo principal é entrar na quadra e dar o seu melhor.
</p>

<p>
"Eu não me importo com legado. Eu costumava. Eu costumava querer abrir uma pista ou espaço neste jogo para mim que as pessoas possam se lembrar, mas agora se tornou uma coisa muito importante. Apenas se torna muito focado em outras pessoas. O que ele fez, o que ele fez? Comparações. Antes, quando não estávamos debatendo tanto, eu me preocupava com isso. Estou prestes a ficar no mesmo fôlego que esses caras tops. Isso foi grande" - confessou Kevin Durant.

Sem concordar com as comparações de hoje em dia entre os jogadores, Durant completou a entrevista dizendo que o importante é dar a sua melhor versão nas quadras e aproveitar a família.

- Hoje em dia eu realmente não me importo (com legado). Eu realmente só quero jogar e produzir, ser o melhor que eu possa ser, ir para casa, aproveitar com minha família, é isso - completou Kevin Durant.
</p>
