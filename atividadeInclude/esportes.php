<p>
O nome em inglês, basketball, significa bola no cesto. É um dos esportes mais populares do mundo. O jogo é disputado por duas equipes de cinco jogadores que tem por objetivo passar a bola por dentro do cesto disposto nas extremidades do campo. </br>
Os cestos ficam a uma altura de três metros e cinco centímetros.
</p>

